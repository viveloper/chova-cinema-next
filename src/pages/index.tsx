import { GetServerSideProps } from 'next';
import Head from 'next/head';
import { dehydrate, DehydratedState, QueryClient, useQuery } from '@tanstack/react-query';
import Carousel from '@/components/Carousel';
import Layout from '@/components/Layout';
import MovieCardList from '@/components/MovieCardList';
import { useRouter } from 'next/router';
import { queryHomePageData } from '@/query/homePage';

export const getServerSideProps: GetServerSideProps<{
  dehydratedState: DehydratedState;
}> = async ({ res }) => {
  res.setHeader('Cache-Control', 'public, s-maxage=60, stale-while-revalidate=300');

  const queryClient = new QueryClient();

  await queryClient.prefetchQuery({
    queryKey: ['pages/home'],
    queryFn: () => queryHomePageData(),
  });

  return {
    props: {
      dehydratedState: dehydrate(queryClient),
    },
  };
};

export default function Home() {
  const { push } = useRouter();

  const { data } = useQuery({
    queryKey: ['pages/home'],
    queryFn: () => queryHomePageData(),
  });

  const moveTicketingPage = () => {
    push('/ticketing');
  };

  const moveMovieDetailPage = (movieCode: string) => {
    push(`/movies/${movieCode}`);
  };

  return (
    <>
      <Head>
        <title>Chova Cinema</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout theme="dark">
        <Carousel theme="dark" height={774} items={data?.carouselItems ?? []} />
        <section style={{ backgroundColor: '#000', padding: '32px 0' }}>
          <div className="center">
            <MovieCardList
              theme="dark"
              movies={data?.movies ?? []}
              showNum={5}
              onTicketingClick={moveTicketingPage}
              onDetailClick={moveMovieDetailPage}
            />
          </div>
        </section>
      </Layout>
    </>
  );
}
